<h1>Utilites</h1>
<p>The <a href="https://jenil.github.io/chota/">chota</a> has very usefull css classes. You can apply these helper classes to almost any element, in order to alter its style.</p>
<ul>
<li><code>text-primary</code> - <span class="text-primary">Primary <strong>text</strong></span></li>
<li><code>text-light</code> - <span class="text-light">Light <code>text</code></span></li>
<li><code>text-white</code> - <span class="bg-primary text-white">White <em>text</em></span></li>
<li><code>text-dark</code> - <span class="text-dark">Dark text</span></li>
</ul>
